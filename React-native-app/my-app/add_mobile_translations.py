# Function to merge translations
def merge_translations(lang_code, new_trans_dict):
    file_path = os.path.join(TRANSLATIONS_DIR, f"{lang_code}.json")
    
    # Load existing
    with open(file_path, 'r', encoding='utf-8') as f:
        existing = json.load(f)
    
    # Merge new translations
    for section, translations in new_trans_dict.items():
        if section not in existing:
            existing[section] = {}
        existing[section].update(translations)
    
    # Save back
    with open(file_path, 'w', encoding='utf-8') as f:
        json.dump(existing, f, indent=2, ensure_ascii=False)
    
    print(f"✓ Updated {lang_code}.json with {sum(len(v) for v in new_trans_dict.values())} new keys")

